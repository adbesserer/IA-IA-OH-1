package proyecto.ia;

/**
 * Clase pair que contiene dos valores Integers.
 */
public class pair {
    public int x;
    public int y;

    /**
     * Pre: ---
     * Post: Crea un pair con los valores x e y como los pasados en el mismo orden por los parámetros.
     * @param xx Próximo valor x del pair creado.
     * @param yy Próximo valor y del pair creado.
     */
    public pair(int xx, int yy){
        x = xx;
        y = yy;
    }
}
